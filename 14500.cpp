int sum(int x, int y, int k) {
    int _sum = 0;
    for (int dy = 0; dy < 4; ++dy) {
        for (int dx = 0; dx < 4; ++dx) {
            _sum += (block[k][dy][dx]) * map[y+dy][x+dx];
        }
    }
    return _sum;
}
 

